def addition(num1, num2)
  sum = num2 + num1
  sum
end

def subtraction(num1, num2)
  sum = num1 - num2
  sum
end

def division(num1, num2)
  sum = num1 / num2
  sum
end

def multiplication(num1, num2)
  sum = num1 * num2
  sum
end

def modulo(num1, num2)
  sum = num1 % num2
  sum
end

def square_root(num)
  Math.sqrt(num)
end